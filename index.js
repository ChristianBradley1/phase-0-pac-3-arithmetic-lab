function add(x, y){
    return x + y;
}

function subtract(x, y){
    return x - y;
}

function multiply(a, b){
    return a * b;
}

function divide(x, y){
    return x / y;
}

function increment(n){
    return ++n;
}

function decrement(n){
    return --n;
}

function makeInt(a){
   return parseInt(a, 10);
    
}

function preserveDecimal(n){
    return parseFloat(n);
}